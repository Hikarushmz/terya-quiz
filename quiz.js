let shuffledQuestions = [];
let currentQuestionIndex = 0;
let score = 0;
let userAnswers = [];

function shuffle(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

function startQuiz() {
    shuffledQuestions = shuffle([...questions]).slice(0, 10); // Ambil 10 soal acak
    loadQuestion();
}

function loadQuestion() {
    if (currentQuestionIndex < shuffledQuestions.length) {
        const questionData = shuffledQuestions[currentQuestionIndex];
        const questionDiv = document.getElementById("quiz-question");

        let optionsHTML = questionData.options.map((option, index) =>
            `<label>
                <input type="radio" name="answer" value="${index}">
                ${option}
            </label><br>`
        ).join("");

        questionDiv.innerHTML = `
            <p><strong>${currentQuestionIndex + 1}. ${questionData.question}</strong></p>
            ${optionsHTML}
        `;

        document.getElementById("next-btn").style.display = "block";
    } else {
        showResults();
    }
}

document.getElementById("next-btn").addEventListener("click", () => {
    let selectedOption = document.querySelector("input[name='answer']:checked");

    if (selectedOption) {
        userAnswers.push(parseInt(selectedOption.value));
        if (userAnswers[currentQuestionIndex] === shuffledQuestions[currentQuestionIndex].correct) {
            score++;
        }
        currentQuestionIndex++;
        loadQuestion();
    } else {
        alert("Pilih jawaban dulu!");
    }
});

function showResults() {
    let resultHTML = `<h2>Hasil Kuis</h2><p>Skor: ${score}/10</p><h3>Jawaban Anda:</h3>`;

    shuffledQuestions.forEach((q, index) => {
        let correctAnswer = q.options[q.correct];
        let userAnswer = q.options[userAnswers[index]];
        let isCorrect = userAnswers[index] === q.correct ? "✅" : "❌";

        resultHTML += `
            <p><strong>${index + 1}. ${q.question}</strong></p>
            <p>Jawaban Anda: <strong>${userAnswer}</strong> ${isCorrect}</p>
            <p>Jawaban Benar: <strong>${correctAnswer}</strong></p>
            <hr>
        `;
    });

    document.getElementById("quiz-question").innerHTML = resultHTML;
    document.getElementById("next-btn").style.display = "none";
}

// Mulai kuis setelah halaman dimuat
startQuiz();
